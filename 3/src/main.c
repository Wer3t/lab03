/* Дано дійсне число а. Користуючись тільки операціями множення, отримати значення: а^4 - за дві операції, 
а^6 та а^8 - за три операції, а^10 - за чотири операції.*/
int main() {
	int  a = 2;
	int a2 = a*2;
	int a4 = a2*a2; //2 операції (5,6 строка)
	int a3 = a2*a;
	int a6 = a3*a3; //3 операції (5,7,8 строка)
	int a8 = a4*a4; //3 операції (5,6,9 строка)
	int a5 = a4*a;
	int a10 = a5*a5; //4 операції (5,6,10,11 строка)
	return 0;
}
